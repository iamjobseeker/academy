package condition.loop;

public class ForEx_01 {
	
	public static void main(String[] args) {
		
		// 반복문, Loop
		// 반복제어문
		// for, while, do-while
		
		// for loop
		
		// for(초기식; 조건식; 증감식) {
		//		반복수행할 코드
		// 		조건식이 참(true)일 경우 반복수행
		// }
		
		int i;
		for ( i=0; i<5; i++ ) {
			System.out.println("Hello: " + i);
		}
		
		System.out.println("i는 " +i);
		
		
		// 수행순서
		// 1. 초기식 수행
		// 2. 조건식 판별
		//  2-1. 조건식이 true라면 3번 진행
		//	2-2. 조건식이 false라면 for문 종료
		// 3. 반복코드 수행 (1회)
		// 4. 증감식 수행
		// 5. 2번 진행
		
		
		
	}

}
