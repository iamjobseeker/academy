package condition.star;

public class Star_10 {

	public static void main(String[] args) {
		
	
			for (int j=0; j<=4; j++) {
//				for
				System.out.print(" ");
				for(int k=5-j; k>=1; k--) {
					System.out.print("*");
				}
				System.out.println();
			}
			
		



	}

}


