package array;

public class ArrayEtc_01 {
	
	public static void main(String[] args) {
		
		int [] arr= {10, 20, 30};
		System.out.println(arr);	// 참조값
		
		String str = "Apple";
		System.out.println(str);	// 문자열
		
		// 문자열은 참조형 타입이지만 출력했을 때 참조값을 출력하지 않는다.
		// 사용자 편의를 위해 문자열을 출력하도록 되어있다.
		
		
		
		
	}

}
