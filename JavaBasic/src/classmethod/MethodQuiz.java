package classmethod;

public class MethodQuiz {

	public static int sum(int num1, int num2) {
		return num1 + num2;
	}
	
	public static int sub(int num1, int num2) {
		return num1 - num2;
	}
	
	public static int mul(int num1, int num2) {
		return num1 * num2;
	}
	
	public static double div(double num1, double num2) {
		return num1 / num2;
	}
	
	
	
	

}
