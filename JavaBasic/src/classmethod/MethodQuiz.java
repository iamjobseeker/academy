package classmethod;

public class MethodQuiz {

	public  int sum(int num1, int num2) {
		return num1 + num2;
	}
	
	public  int sub(int num1, int num2) {
		return num1 - num2;
	}
	
	public  int mul(int num1, int num2) {
		return num1 * num2;
	}
	
	public  double div(double num1, double num2) {
		return num1 / num2;
	}
	
}
